    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
                                                                                         
    // Obtener el ID del cliente desde el cuadro de texto
    String idText = txtidcliente.getText().trim();

    if (idText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor ingrese un ID de cliente.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Llamar al mÃ©todo delete del controlador
    EntityManager em = null;
    EntityTransaction tx = null;

    try {
        em = clienteController.getEntityManager();
        tx = em.getTransaction();
        tx.begin();

        Cliente cliente = em.find(Cliente.class, idText);

        if (cliente == null) {
            JOptionPane.showMessageDialog(this, "Cliente no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        clienteController.delete(idText); // Llamar al mÃ©todo delete del controlador
        tx.commit();
        JOptionPane.showMessageDialog(this, "Cliente eliminado con Ã©xito.", "Ãxito", JOptionPane.INFORMATION_MESSAGE);

    } catch (Exception e) {
        if (tx != null && tx.isActive()) {
            tx.rollback();
        }
        JOptionPane.showMessageDialog(this, "Error al eliminar el cliente: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    } finally {
        if (em != null) {
            em.close();
        }
    }



    }//GEN-LAST:event_btneliminarActionPerformed

