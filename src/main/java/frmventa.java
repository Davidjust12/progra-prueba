    private void actualizarTotal(BigDecimal cambio) {
        // Obtener el total actual
        BigDecimal totalActual;
        try {
            totalActual = new BigDecimal(txttotal.getText());
        } catch (NumberFormatException e) {
            totalActual = BigDecimal.ZERO; // Si no se puede convertir, iniciar en cero
        }

        // Actualizar el total
        BigDecimal nuevoTotal = totalActual.add(cambio); // Sumar el subtotal
        txttotal.setText(nuevoTotal.toString()); // Mostrar el nuevo total
    }



