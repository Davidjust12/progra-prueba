    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
 // Obtener el ID del cliente y del usuario desde los campos de texto
String idCliente = txtidcliente.getText(); // Asumiendo que este campo es String
Integer idUsuario = Integer.parseInt(txtidusuario.getText()); // Asumiendo que este campo es Integer

// Crear la instancia de la venta
Ventas venta = new Ventas();
venta.setIdcliente(new Cliente(idCliente)); // Crear objeto Cliente con ID
venta.setIdusuario(new Usuarios(idUsuario)); // Crear objeto Usuarios con ID
venta.setNumDocumento(txtnumerodocumento.getText()); // Establecer el número de documento
venta.setTipoDocumento(txttipodocumento.getText()); // Establecer el tipo de documento
venta.setTotal(new BigDecimal(txttotal.getText())); // Establecer el total de la venta
venta.setFecha(null); // Establecer la fecha como nula
venta.setHora(null); // Establecer la hora como nula

// Inicializar el controlador de Ventas
EntityManagerFactory emf = Persistence.createEntityManagerFactory("CapaLogica_ventas_jar_1.0-SNAPSHOTPU");
VentasJpaController ventasController = new VentasJpaController(emf);

try {
    // Guardar la venta
    ventasController.create(venta);

    // Obtener los datos de la tabla
    DefaultTableModel model = (DefaultTableModel) tabladetalle.getModel();
    for (int i = 0; i < model.getRowCount(); i++) {
        DetalleVentas detalle = new DetalleVentas();
        detalle.setCantidad((Integer) model.getValueAt(i, 2)); // Cantidad
        detalle.setPrecio((BigDecimal) model.getValueAt(i, 3)); // Precio
        // Suponiendo que el modelo contiene el objeto Producto y no solo su ID
        detalle.setIdproducto((Producto) model.getValueAt(i, 0)); // Objeto Producto
        detalle.setIdventa(venta); // Relación con la venta

        // Inicializar el controlador de DetalleVentas
        DetalleVentasJpaController detalleVentasController = new DetalleVentasJpaController(emf);

        // Guardar el detalle de la venta
        detalleVentasController.create(detalle);
    }
    
    // Mostrar un mensaje de éxito
    JOptionPane.showMessageDialog(this, "Venta guardada exitosamente.");
    
} catch (Exception e) {
    // Mostrar un mensaje de error si algo falla
    JOptionPane.showMessageDialog(this, "Error al guardar la venta: " + e.getMessage());
} finally {
    // Asegúrate de cerrar el EntityManagerFactory para liberar recursos
    if (emf != null) {
        emf.close();
    }
}




    }//GEN-LAST:event_btnguardarActionPerformed

